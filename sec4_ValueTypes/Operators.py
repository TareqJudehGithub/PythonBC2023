# Operators in Python

print (5 ** 2)  # 5 to the power of 2  (5 squared).
print (5 // 2)  # Whole number results

# PEMDAS Rule(parentheses, exponents, multiplication, division, addition, and subtraction).

# Workout 2*2+2/2-2
workout_result = (2 * 2) + (2 / 2) - 2
print(f"Operation result is = {workout_result}")

# Addition for concatenation
greet = "Hello " + "world!"
print(greet)
